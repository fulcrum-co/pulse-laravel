    public function getTerm(string $key): string
    {
        $terminology = $this->terminology ?? [];

        return $terminology[$key] ?? self::DEFAULT_TERMINOLOGY[$key] ?? ucfirst(str_replace('_', ' ', $key));
    }

    /**
     * Get all terminology labels (merged with defaults).
